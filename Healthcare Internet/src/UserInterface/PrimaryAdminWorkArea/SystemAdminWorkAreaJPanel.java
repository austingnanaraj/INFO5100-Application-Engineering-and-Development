    private void manageAdminJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_manageAdminJButtonActionPerformed
        // TODO add your handling code here:
        int a = system.getNetworkList().size();
        if (a <= 0) {
            JOptionPane.showMessageDialog(null, "Please add a network");
            return;
        }
        boolean enterpriseCheck = true;
        for (Network n : system.getNetworkList()) {
            int networkLength = n.getEnterpriseDirectory().getEnterpriseList().size();
            if (networkLength > 0) {
                enterpriseCheck = false;
                break;
            }
        }
        if (enterpriseCheck == true) {
            JOptionPane.showMessageDialog(null, "Please add an enterprise");
            return;
        }
        ManageEnterpriseAdminJPanel manageEnterpriseAdminJPanel = new ManageEnterpriseAdminJPanel(userProcessContainer, system);
        userProcessContainer.add("manageEnterpriseAdminJPanel", manageEnterpriseAdminJPanel);

        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_manageAdminJButtonActionPerformed

