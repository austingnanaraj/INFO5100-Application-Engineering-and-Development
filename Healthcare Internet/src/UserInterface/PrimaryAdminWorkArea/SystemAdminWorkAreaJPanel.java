    private void populateTable1() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        for (WorkRequest work : system.getMessageDirectory().getWorkRequestList()) {
            Message msg = (Message) work;
            Object[] row = new Object[3];
            row[0] = msg;
            row[1] = msg.getSender();
            row[2] = msg.getReceiver();
            dtm.addRow(row);
        }
    }

