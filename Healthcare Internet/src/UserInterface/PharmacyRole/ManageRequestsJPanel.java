    private void btnCheckAvailabilityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckAvailabilityActionPerformed
        int selectedRow = tblRequests.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }
        de = (DoctorPrescriptionToPharmacy) tblRequests.getValueAt(selectedRow, 0);
        boolean a = true;
        int b = enterprise.getStockDirectory().getStockDirectory().size();
        if (b > 0) {
            for (Stock s : enterprise.getStockDirectory().getStockDirectory()) {
                if (de.getDrug().getDrugName().equals(s.getDrug().getDrugName()) && de.getQuantityOfDrugs() <= s.getQuantity()) {
                    JOptionPane.showMessageDialog(null, "Stock is Available");
                    this.s = s;
                    btnProcessRequest.setEnabled(true);
                    break;
                }
            }
        } else {
            a = false;
        }
        if (a == false) {
            JOptionPane.showMessageDialog(null, "Stock is not available");
            return;
        }


    }//GEN-LAST:event_btnCheckAvailabilityActionPerformed

