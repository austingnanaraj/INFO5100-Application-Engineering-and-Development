    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblRequests.getModel();
        model.setRowCount(0);
        for (WorkRequest e : eco.getDrugPrescriptionDirectory().getWorkRequestList()) {
            DoctorPrescriptionToPharmacy eb = (DoctorPrescriptionToPharmacy) e;
            if (eb.getQuantityOfDrugs() > 0 && eb.getStatus() == "requested") {
                Object row[] = new Object[4];
                row[0] = eb;
                row[1] = eb.getDoctor().getName();
                row[2] = eb.getQuantityOfDrugs();
                row[3] = eb.getHospitalEnterprise().getName();
                model.addRow(row);
            }
        }
    }

