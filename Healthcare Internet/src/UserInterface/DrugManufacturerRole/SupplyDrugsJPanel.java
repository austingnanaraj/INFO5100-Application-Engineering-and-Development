//                            s.setQuantity(s.getQuantity() + q);
//                            break;
//                        }
//                    }
//                }
//            }
        int a = prm.getPharmacyEnterprise().getStockDirectory().getStockDirectory().size();
        if (a > 0) {
            for (Stock s : prm.getPharmacyEnterprise().getStockDirectory().getStockDirectory()) {
                if (s.getDrug().getDrugName().equals(prm.getDrug().getDrugName())) {
                    s.setQuantity(s.getQuantity() + q);
                    JOptionPane.showMessageDialog(null, "Request Complete");
                } else {
                    Stock ss = new Stock();
                    prm.getPharmacyEnterprise().getStockDirectory().getStockDirectory().add(ss);
                    ss.setDrug(prm.getDrug());
                    ss.setQuantity(q);
                    JOptionPane.showMessageDialog(null, "Request Complete");
                }
            }
        } else {
            Stock ss = new Stock();
            prm.getPharmacyEnterprise().getStockDirectory().getStockDirectory().add(ss);
            ss.setDrug(prm.getDrug());
            ss.setQuantity(q);
            JOptionPane.showMessageDialog(null, "Request Complete");
        }


