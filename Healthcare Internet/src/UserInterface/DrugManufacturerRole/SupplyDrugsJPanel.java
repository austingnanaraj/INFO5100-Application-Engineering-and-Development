    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);

        for (WorkRequest e : eco.getDrugRequestToManufacturer().getWorkRequestList()) {
            PharmacyRequestToManufacturerer pe = (PharmacyRequestToManufacturerer) e;
            if (!pe.getStatus().equals("completed")) {
                Object row[] = new Object[3];
                row[0] = pe;
                row[1] = pe.getPharmacyEnterprise().getName();
                row[2] = pe.getQuantityOfDrugs();
                dtm.addRow(row);
            }

        }

    }

    private void populatecombo() {
        jComboBox1.removeAllItems();
        for (Network n : eco.getNetworkList()) {
            jComboBox1.addItem(n);
        }
    }

    private void populatePharmacy(Network n) {
        jComboBox2.removeAllItems();
        for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()) {
            if (e instanceof PharmacyEnterprise) {
                jComboBox2.addItem(e);
            }
        }
    }

    private void populateDrug() {
        jComboBox3.removeAllItems();
        for (Drug d : e.getDrugDirectory().getDrugDirectory()) {
            jComboBox3.addItem(d);
        }
    }

