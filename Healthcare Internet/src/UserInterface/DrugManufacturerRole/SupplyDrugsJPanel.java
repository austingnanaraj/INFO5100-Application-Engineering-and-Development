    private void btnAddStocksActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddStocksActionPerformed
        Network n = (Network) jComboBox1.getSelectedItem();
        PharmacyEnterprise pe = (PharmacyEnterprise) jComboBox2.getSelectedItem();
        Drug d = (Drug) jComboBox3.getSelectedItem();
        for (Network ne : eco.getNetworkList()) {
            if (ne.getName().equals(n.getName())) {
                for (Enterprise pet : ne.getEnterpriseDirectory().getEnterpriseList()) {
                    if (pet.getName().equals(pe.getName())) {
                        for (Stock s : pe.getStockDirectory().getStockDirectory()) {
                            if (s.getDrug().getDrugName().equals(d.getDrugName())) {
                                Stock ss = new Stock();
                                ((PharmacyEnterprise) pet).getStockDirectory().getStockDirectory().add(ss);
                                ss.setDrug(d);
                                ss.setQuantity((int) jSpinner1.getValue());
                                JOptionPane.showMessageDialog(null, "Drug added");
                                break;
                            }
                        }

                    }
                }
            }
        }

    }//GEN-LAST:event_btnAddStocksActionPerformed


