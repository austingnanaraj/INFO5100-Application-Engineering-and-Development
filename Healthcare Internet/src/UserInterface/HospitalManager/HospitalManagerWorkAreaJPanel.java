    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        for (WorkRequest work : eco.getMessageDirectory().getWorkRequestList()) {
            if (work.getReceiver() == null || work.getReceiver() == userAccount) {
                Message msg = (Message) work;
                Object[] row = new Object[2];
                row[0] = msg;
                row[1] = msg.getSender();
                dtm.addRow(row);
            }
        }
    }

