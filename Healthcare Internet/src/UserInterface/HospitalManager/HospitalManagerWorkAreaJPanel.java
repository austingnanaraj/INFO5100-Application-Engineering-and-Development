    private void btnReplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReplyActionPerformed
        //DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        //dtm.setRowCount(0);
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        } else if (txtReply.getText() == null) {
            JOptionPane.showMessageDialog(null, "Message cannot be empty");
            return;
        } else {
            Message m = (Message) jTable1.getValueAt(selectedRow, 0);
            Message mess2 = new Message();
            eco.getMessageDirectory().getWorkRequestList().add(mess2);
            mess2.setMessage(txtReply.getText());
            mess2.setSender(userAccount);
            mess2.setReceiver(m.getSender());
        }


    }//GEN-LAST:event_btnReplyActionPerformed

